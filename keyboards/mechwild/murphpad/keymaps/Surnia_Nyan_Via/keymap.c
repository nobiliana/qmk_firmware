bool process_record_user(uint16_t keycode, keyrecord_t *record) { //macro areas. names defined in enum, function defined here. 
        switch (keycode) {
                case DZERO:
                        if (record->event.pressed){
                                SEND_STRING("00"); //SS = press and hold, then send respective string inside
                        }
                break;
        }
        return true;
};

bool wpm_keycode_user(uint16_t keycode) { //needed to ENABLE NUMPAD CODES FOR WPM DETECTION. WHYYYYYY
    if ((keycode >= QK_MOD_TAP && keycode <= QK_MOD_TAP_MAX) || (keycode >= QK_LAYER_TAP && keycode <= QK_LAYER_TAP_MAX) || (keycode >= QK_MODS && keycode <= QK_MODS_MAX)) {
        keycode = keycode & 0xFF;
    } else if (keycode > 0xFF) {
        keycode = 0;
    }
    if ((keycode >= KC_A && keycode <= KC_0) || (keycode >= KC_TAB && keycode <= KC_SLASH) || (keycode >= KC_PSLS && keycode <= KC_PDOT)) {//third range is numpad.
        return true;
    }

    return false;
}

